// ============================================================================
//  ПЕСОЧНИЦА: упрощённый Sticker (эмодзи-фолбэк).
//  В боевом проекте этот компонент тянет анимированные TGS-стикеры с бэкенда.
//  Бэкенда тут нет, поэтому рисуем эмодзи того же размера — чтобы вёрстка
//  выглядела цельно. API компонента (props) совпадает с боевым, так что экраны
//  с <Sticker .../> вставятся в проект без изменений.
//
//  ВАЖНО: этот файл — заглушка песочницы. Обратно в проект его НЕ копировать.
// ============================================================================

// Грубое соответствие index → эмодзи (только для наглядности песочницы).
const BY_INDEX = {
  0: '🐱',
  1: '😢',
  2: '🔍',
  3: '❤️‍🔥',
  9: '💌',
  12: '🐱',
}

export default function Sticker({ index, emoji, size = 80, style }) {
  const glyph = emoji || BY_INDEX[index] || '✨'
  return (
    <div
      style={{
        width: size,
        height: size,
        display: 'inline-flex',
        alignItems: 'center',
        justifyContent: 'center',
        flexShrink: 0,
        fontSize: Math.round(size * 0.62),
        lineHeight: 1,
        userSelect: 'none',
        ...style,
      }}
    >
      {glyph}
    </div>
  )
}
